// String - Representar um texto
// int - Apresentar um Inteiro
// double - Apresenta numeros decimais (numero com virgula)
// num - Pode representar qualquer numero
// bool - Condicao de falso ou verdadeiro ( true - false)
// dynamic - Aceita qualquer coisa

void main() {
  // camelCase
  int numeroDoSapato = 42;

  //snake_case
  double valor_lanche = 48.95;

  //Representando Boleano
  bool estaCalor = false;

  num numeroAleatorio = 9.99;

  dynamic valorAleatorio = true;
}
